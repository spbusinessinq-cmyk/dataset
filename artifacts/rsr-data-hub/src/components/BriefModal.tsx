import {
  Dialog,
  DialogContent,
  DialogDescription,
  DialogHeader,
  DialogTitle,
} from "@/components/ui/dialog";
import { Button } from "@/components/ui/button";
import { Signal } from "@/lib/mock-data";
import { Copy, X, AlertTriangle, FileText, Radio, Tag, ShieldAlert, Printer } from "lucide-react";
import { Badge } from "@/components/ui/badge";
import { useToast } from "@/hooks/use-toast";

interface BriefModalProps {
  signal: Signal | null;
  open: boolean;
  onClose: () => void;
}

function buildPlainText(sig: Signal): string {
  const formatDate = (iso: string) => {
    try { return new Date(iso).toUTCString(); } catch { return iso; }
  };
  return `INTELLIGENCE BRIEF — RSR DATA HUB
===================================
Signal: ${sig.id}
Date:   ${formatDate(sig.timestamp)}

CLASSIFICATION: ${sig.classification}
ENGINE:         ${String(sig.engine).toUpperCase()}
CONFIDENCE:     ${sig.confidence}%
SOURCE:         ${sig.source} (${sig.sourceType ?? "—"})

HEADLINE
--------
${sig.title}

EXECUTIVE SUMMARY
-----------------
${sig.summary}

WHY IT MATTERS
--------------
${sig.whyItMatters}

KEY SIGNALS / IMPACT TAGS
--------------------------
${sig.tags.join(" | ")}

EXTRACTED ENTITIES
------------------
${sig.entities.map((e) => `• ${e}`).join("\n")}

SYSTEM IMPACT CATEGORIES
-------------------------
${sig.systemImpact.map((i) => `• ${i}`).join("\n")}

OPERATOR TAKEAWAY
-----------------
Classification ${sig.classification} at ${sig.confidence}% confidence. ${
    sig.classification === "CRITICAL"
      ? "Immediate escalation recommended. Review system impact categories and entity list for attribution."
      : sig.classification === "ELEVATED"
      ? "Monitor closely. Follow up within 24 hours and cross-reference against known threat indicators."
      : "Continue routine monitoring. Archive for pattern analysis."
  }

---
Generated by RSR Data Hub v1.0.0`.trim();
}

function buildPrintHtml(sig: Signal): string {
  const formatDate = (iso: string) => {
    try { return new Date(iso).toUTCString(); } catch { return iso; }
  };
  const classColor =
    sig.classification === "CRITICAL" ? "#ef4444" :
    sig.classification === "ELEVATED" ? "#f59e0b" :
    sig.classification === "WATCH"    ? "#60a5fa" : "#22c55e";
  const takeaway =
    sig.classification === "CRITICAL"
      ? "Immediate escalation recommended. Review system impact categories and entity list for attribution."
      : sig.classification === "ELEVATED"
      ? "Monitor closely. Follow up within 24 hours and cross-reference against known threat indicators."
      : "Continue routine monitoring. Archive for pattern analysis.";

  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8"/>
<title>rsr-brief-${sig.id}</title>
<style>
  * { margin: 0; padding: 0; box-sizing: border-box; }
  body {
    font-family: 'Courier New', Courier, monospace;
    font-size: 11pt;
    color: #1a1a1a;
    background: #fff;
    padding: 32pt 40pt;
    max-width: 680pt;
    margin: 0 auto;
  }
  .header { border-bottom: 2pt solid #000; padding-bottom: 10pt; margin-bottom: 16pt; }
  .header h1 { font-size: 13pt; letter-spacing: 2px; text-transform: uppercase; }
  .header .meta { font-size: 9pt; color: #555; margin-top: 4pt; }
  .badge {
    display: inline-block;
    border: 1pt solid currentColor;
    padding: 1pt 6pt;
    font-size: 9pt;
    font-weight: bold;
    letter-spacing: 1px;
    text-transform: uppercase;
    border-radius: 2pt;
  }
  .section { margin-bottom: 14pt; }
  .section-label {
    font-size: 8pt;
    letter-spacing: 2px;
    text-transform: uppercase;
    color: #666;
    margin-bottom: 4pt;
    font-weight: bold;
  }
  .section-label.accent { color: #16a34a; }
  .section p { font-family: Georgia, serif; font-size: 10.5pt; line-height: 1.55; color: #222; }
  .accent-block {
    border-left: 3pt solid #16a34a;
    padding: 6pt 10pt;
    background: #f0fdf4;
    font-family: Georgia, serif;
    font-size: 10.5pt;
    line-height: 1.55;
    color: #222;
  }
  .tags { display: flex; flex-wrap: wrap; gap: 4pt; }
  .tag {
    border: 1pt solid #999;
    padding: 1pt 6pt;
    font-size: 8.5pt;
    border-radius: 2pt;
    color: #444;
  }
  .two-col { display: grid; grid-template-columns: 1fr 1fr; gap: 16pt; }
  .entity { display: flex; align-items: center; gap: 5pt; font-family: Georgia, serif; font-size: 10pt; color: #222; }
  .dot { width: 5pt; height: 5pt; border-radius: 50%; background: #666; flex-shrink: 0; }
  .takeaway-block {
    border: 1pt solid #ddd;
    padding: 10pt 12pt;
    background: #fafafa;
    font-family: Georgia, serif;
    font-size: 10.5pt;
    line-height: 1.6;
  }
  .divider { border-top: 1pt solid #ddd; margin: 12pt 0; }
  .footer { font-size: 8pt; color: #aaa; margin-top: 20pt; }
  @media print {
    body { padding: 0; }
  }
</style>
</head>
<body>
<div class="header">
  <div style="display:flex; justify-content:space-between; align-items:flex-start;">
    <div>
      <h1>Intelligence Brief</h1>
      <div class="meta">RSR Data Hub · Signal ${sig.id} · ${formatDate(sig.timestamp)}</div>
    </div>
    <span class="badge" style="color:${classColor}; border-color:${classColor};">${sig.classification}</span>
  </div>
  <div class="meta" style="margin-top:6pt;">
    Engine: ${String(sig.engine).toUpperCase()} &nbsp;·&nbsp; Confidence: ${sig.confidence}% &nbsp;·&nbsp;
    Source: ${sig.source}${sig.sourceType ? ` (${sig.sourceType})` : ""}
  </div>
</div>

<div class="section">
  <div class="section-label">Headline</div>
  <p style="font-size:13pt; font-weight:bold; font-family:Georgia,serif; color:#111;">${sig.title}</p>
</div>

<div class="section">
  <div class="section-label">Executive Summary</div>
  <p>${sig.summary}</p>
</div>

<div class="section">
  <div class="section-label accent">Why It Matters</div>
  <div class="accent-block">${sig.whyItMatters}</div>
</div>

<div class="section">
  <div class="section-label">Key Signals / Impact Tags</div>
  <div class="tags">${sig.tags.map((t) => `<span class="tag">${t}</span>`).join("")}</div>
</div>

<div class="section two-col">
  <div>
    <div class="section-label">Extracted Entities</div>
    ${sig.entities.map((e) => `<div class="entity"><span class="dot"></span>${e}</div>`).join("")}
  </div>
  <div>
    <div class="section-label">System Impact</div>
    ${sig.systemImpact.map((i) => `<div class="entity"><span class="dot" style="background:#16a34a;"></span>${i}</div>`).join("")}
  </div>
</div>

<div class="divider"></div>

<div class="section">
  <div class="section-label accent">Operator Takeaway</div>
  <div class="takeaway-block">
    Classification <strong>${sig.classification}</strong> at <strong>${sig.confidence}%</strong> confidence. ${takeaway}
  </div>
</div>

<div class="footer">Generated by RSR Data Hub v1.0.0 &nbsp;·&nbsp; ${sig.id} &nbsp;·&nbsp; ${formatDate(sig.timestamp)}</div>
</body>
</html>`;
}

export default function BriefModal({ signal, open, onClose }: BriefModalProps) {
  const { toast } = useToast();

  const handleCopy = () => {
    if (!signal) return;
    navigator.clipboard.writeText(buildPlainText(signal)).then(() => {
      toast({
        title: "Brief Copied",
        description: "Intelligence brief copied to clipboard",
        className: "font-mono border-primary bg-background text-foreground",
      });
    });
  };

  const handleDownloadPdf = () => {
    if (!signal) return;
    const html = buildPrintHtml(signal);
    const newWin = window.open("", "_blank", "width=900,height=700,scrollbars=yes");
    if (!newWin) {
      toast({
        title: "Pop-up Blocked",
        description: "Allow pop-ups for this site and try again",
        variant: "destructive",
        className: "font-mono",
      });
      return;
    }
    newWin.document.write(html);
    newWin.document.close();
    // Wait for content to render before printing
    newWin.onload = () => {
      setTimeout(() => {
        newWin.focus();
        newWin.print();
      }, 300);
    };
    toast({
      title: "PDF Ready",
      description: `Print dialog opened — save as rsr-brief-${signal.id}.pdf`,
      className: "font-mono border-primary bg-background text-foreground",
    });
  };

  const formatDate = (iso: string) => {
    try { return new Date(iso).toUTCString(); } catch { return iso; }
  };

  return (
    <Dialog open={open} onOpenChange={(v) => { if (!v) onClose(); }}>
      <DialogContent
        className="max-w-2xl bg-card border border-card-border text-foreground"
        data-testid="modal-brief"
      >
        <DialogHeader className="border-b border-card-border pb-3">
          <DialogTitle className="font-mono text-sm text-primary tracking-widest flex items-center gap-2">
            <FileText className="w-4 h-4" />
            INTELLIGENCE BRIEF {signal ? `— ${signal.id}` : ""}
          </DialogTitle>
          <DialogDescription className="sr-only">
            Formatted intelligence brief for signal {signal?.id ?? ""}
          </DialogDescription>
        </DialogHeader>

        {!signal ? (
          <div className="flex items-center justify-center py-10">
            <span className="font-mono text-xs text-muted-foreground">No signal selected.</span>
          </div>
        ) : (
          <div className="flex flex-col gap-4 max-h-[68vh] overflow-y-auto pr-1 mt-2">

            {/* Meta row */}
            <div className="flex items-center gap-2 flex-wrap pb-3 border-b border-card-border/50">
              <Badge
                variant="outline"
                className={`font-mono text-[9px] px-2 py-0.5 rounded-sm border uppercase font-bold ${
                  signal.classification === "CRITICAL" ? "text-destructive border-destructive bg-destructive/10" :
                  signal.classification === "ELEVATED" ? "text-amber-500 border-amber-500 bg-amber-500/10" :
                  signal.classification === "WATCH"    ? "text-blue-400 border-blue-400 bg-blue-400/10" :
                  "text-primary border-primary bg-primary/10"
                }`}
              >
                {signal.classification}
              </Badge>
              <span className="font-mono text-[10px] text-muted-foreground">{signal.confidence}% CONF</span>
              <span className="text-muted-foreground/30">·</span>
              <span className="font-mono text-[10px] text-muted-foreground flex items-center gap-1">
                <Radio className="w-3 h-3" /> {signal.source}
              </span>
              <span className="text-muted-foreground/30">·</span>
              <span className="font-mono text-[10px] text-muted-foreground">{String(signal.engine).toUpperCase()}</span>
              <span className="text-muted-foreground/30">·</span>
              <span className="font-mono text-[10px] text-muted-foreground">{formatDate(signal.timestamp)}</span>
            </div>

            {/* Headline */}
            <div className="flex flex-col gap-1.5">
              <span className="font-mono text-[9px] text-muted-foreground tracking-widest">HEADLINE</span>
              <h3 className="font-sans font-bold text-base leading-snug text-foreground">{signal.title}</h3>
            </div>

            {/* Executive Summary */}
            <div className="flex flex-col gap-1.5">
              <span className="font-mono text-[9px] text-muted-foreground tracking-widest">EXECUTIVE SUMMARY</span>
              <p className="font-sans text-sm text-secondary-foreground leading-relaxed">{signal.summary}</p>
            </div>

            {/* Why It Matters */}
            <div className="flex flex-col gap-1.5">
              <span className="font-mono text-[9px] text-primary tracking-widest flex items-center gap-1">
                <AlertTriangle className="w-3 h-3" /> WHY IT MATTERS
              </span>
              <p className="font-sans text-sm text-foreground leading-relaxed border-l-2 border-primary/50 pl-3 py-1 bg-primary/5 rounded-r">
                {signal.whyItMatters}
              </p>
            </div>

            {/* Key Signals */}
            <div className="flex flex-col gap-1.5">
              <span className="font-mono text-[9px] text-muted-foreground tracking-widest flex items-center gap-1">
                <Tag className="w-3 h-3" /> KEY SIGNALS
              </span>
              <div className="flex flex-wrap gap-1.5">
                {signal.tags.map((t) => (
                  <Badge key={t} variant="secondary" className="font-mono text-[9px] rounded-sm">{t}</Badge>
                ))}
              </div>
            </div>

            {/* Entities + Impact */}
            <div className="grid grid-cols-2 gap-4">
              <div className="flex flex-col gap-1.5">
                <span className="font-mono text-[9px] text-muted-foreground tracking-widest">EXTRACTED ENTITIES</span>
                <div className="flex flex-col gap-1">
                  {signal.entities.map((e) => (
                    <span key={e} className="font-sans text-xs text-foreground flex items-center gap-1.5">
                      <span className="w-1 h-1 shrink-0 rounded-full bg-muted-foreground" />{e}
                    </span>
                  ))}
                </div>
              </div>
              <div className="flex flex-col gap-1.5">
                <span className="font-mono text-[9px] text-muted-foreground tracking-widest flex items-center gap-1">
                  <ShieldAlert className="w-3 h-3" /> SYSTEM IMPACT
                </span>
                <div className="flex flex-col gap-1">
                  {signal.systemImpact.map((i) => (
                    <span key={i} className="font-sans text-xs text-secondary-foreground flex items-center gap-1.5">
                      <span className="w-1 h-1 shrink-0 rounded-full bg-primary/60" />{i}
                    </span>
                  ))}
                </div>
              </div>
            </div>

            {/* Operator Takeaway */}
            <div className="flex flex-col gap-1.5 border-t border-card-border/50 pt-3">
              <span className="font-mono text-[9px] text-primary tracking-widest">OPERATOR TAKEAWAY</span>
              <p className="font-sans text-sm text-foreground leading-relaxed">
                Classification <strong>{signal.classification}</strong> at <strong>{signal.confidence}%</strong> confidence.{" "}
                {signal.classification === "CRITICAL"
                  ? "Immediate escalation recommended. Review system impact categories and entity list for attribution."
                  : signal.classification === "ELEVATED"
                  ? "Monitor closely. Follow up within 24 hours and cross-reference against known threat indicators."
                  : "Continue routine monitoring. Archive for pattern analysis."}
              </p>
            </div>

            <p className="font-mono text-[9px] text-muted-foreground/40 pt-1 border-t border-card-border/40">
              Generated by RSR Data Hub v1.0.0 · {signal.id}
            </p>
          </div>
        )}

        {/* Action buttons */}
        <div className="flex items-center justify-between pt-3 border-t border-card-border">
          <span className="font-mono text-[9px] text-muted-foreground/50">
            Save as PDF: use browser Save as PDF option
          </span>
          <div className="flex gap-2">
            <Button
              variant="outline"
              className="font-mono text-xs border-primary/40 hover:bg-primary/10"
              onClick={handleCopy}
              disabled={!signal}
              data-testid="btn-copy-brief"
            >
              <Copy className="w-3.5 h-3.5 mr-2" />
              COPY BRIEF
            </Button>
            <Button
              variant="outline"
              className="font-mono text-xs border-primary/60 bg-primary/10 hover:bg-primary/20 text-primary"
              onClick={handleDownloadPdf}
              disabled={!signal}
              data-testid="btn-download-pdf"
            >
              <Printer className="w-3.5 h-3.5 mr-2" />
              DOWNLOAD PDF
            </Button>
            <Button
              variant="ghost"
              className="font-mono text-xs text-muted-foreground hover:text-foreground border border-card-border"
              onClick={onClose}
              data-testid="btn-close-brief"
            >
              <X className="w-3.5 h-3.5 mr-2" />
              CLOSE
            </Button>
          </div>
        </div>
      </DialogContent>
    </Dialog>
  );
}
