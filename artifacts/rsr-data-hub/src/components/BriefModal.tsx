import {
  Dialog,
  DialogContent,
  DialogDescription,
  DialogHeader,
  DialogTitle,
} from "@/components/ui/dialog";
import { Button } from "@/components/ui/button";
import { Signal } from "@/lib/mock-data";
import { Copy, X, AlertTriangle, FileText, Radio, Tag, ShieldAlert } from "lucide-react";
import { Badge } from "@/components/ui/badge";
import { useToast } from "@/hooks/use-toast";

interface BriefModalProps {
  signal: Signal | null;
  open: boolean;
  onClose: () => void;
}

export default function BriefModal({ signal, open, onClose }: BriefModalProps) {
  const { toast } = useToast();

  const formatDate = (iso: string) => {
    try {
      return new Date(iso).toUTCString();
    } catch {
      return iso;
    }
  };

  const buildPlainText = (sig: Signal) => `
INTELLIGENCE BRIEF — RSR DATA HUB
===================================
Signal: ${sig.id}
Date:   ${formatDate(sig.timestamp)}

CLASSIFICATION: ${sig.classification}
ENGINE:         ${String(sig.engine).toUpperCase()}
CONFIDENCE:     ${sig.confidence}%
SOURCE:         ${sig.source} (${sig.sourceType ?? "—"})

HEADLINE
--------
${sig.title}

EXECUTIVE SUMMARY
-----------------
${sig.summary}

WHY IT MATTERS
--------------
${sig.whyItMatters}

KEY SIGNALS / IMPACT TAGS
--------------------------
${sig.tags.join(" | ")}

EXTRACTED ENTITIES
------------------
${sig.entities.map((e) => `• ${e}`).join("\n")}

SYSTEM IMPACT CATEGORIES
-------------------------
${sig.systemImpact.map((i) => `• ${i}`).join("\n")}

OPERATOR TAKEAWAY
-----------------
Classification ${sig.classification} at ${sig.confidence}% confidence. ${
    sig.classification === "CRITICAL"
      ? "Immediate escalation recommended. Review system impact categories and entity list for attribution."
      : sig.classification === "ELEVATED"
      ? "Monitor closely. Follow up within 24 hours and cross-reference against known threat indicators."
      : "Continue routine monitoring. Archive for pattern analysis."
  }

---
Generated by RSR Data Hub v1.0.0
`.trim();

  const handleCopy = () => {
    if (!signal) return;
    navigator.clipboard.writeText(buildPlainText(signal)).then(() => {
      toast({
        title: "Brief Copied",
        description: "Intelligence brief copied to clipboard",
        className: "font-mono border-primary bg-background text-foreground",
      });
    });
  };

  return (
    <Dialog open={open} onOpenChange={(v) => { if (!v) onClose(); }}>
      <DialogContent
        className="max-w-2xl bg-card border border-card-border text-foreground"
        data-testid="modal-brief"
      >
        <DialogHeader className="border-b border-card-border pb-3">
          <DialogTitle className="font-mono text-sm text-primary tracking-widest flex items-center gap-2">
            <FileText className="w-4 h-4" />
            INTELLIGENCE BRIEF {signal ? `— ${signal.id}` : ""}
          </DialogTitle>
          <DialogDescription className="sr-only">
            Formatted intelligence brief for signal {signal?.id ?? ""}
          </DialogDescription>
        </DialogHeader>

        {!signal ? (
          <div className="flex items-center justify-center py-10">
            <span className="font-mono text-xs text-muted-foreground">No signal selected.</span>
          </div>
        ) : (
          <div className="flex flex-col gap-4 max-h-[70vh] overflow-y-auto pr-1 mt-2">

            {/* Meta row */}
            <div className="flex items-center gap-2 flex-wrap pb-3 border-b border-card-border/50">
              <Badge
                variant="outline"
                className={`font-mono text-[9px] px-2 py-0.5 rounded-sm border uppercase font-bold ${
                  signal.classification === "CRITICAL" ? "text-destructive border-destructive bg-destructive/10" :
                  signal.classification === "ELEVATED" ? "text-amber-500 border-amber-500 bg-amber-500/10" :
                  signal.classification === "WATCH"    ? "text-blue-400 border-blue-400 bg-blue-400/10" :
                  "text-primary border-primary bg-primary/10"
                }`}
              >
                {signal.classification}
              </Badge>
              <span className="font-mono text-[10px] text-muted-foreground">
                {signal.confidence}% CONF
              </span>
              <span className="text-muted-foreground/30">·</span>
              <span className="font-mono text-[10px] text-muted-foreground flex items-center gap-1">
                <Radio className="w-3 h-3" /> {signal.source}
              </span>
              <span className="text-muted-foreground/30">·</span>
              <span className="font-mono text-[10px] text-muted-foreground">
                {String(signal.engine).toUpperCase()}
              </span>
              <span className="text-muted-foreground/30">·</span>
              <span className="font-mono text-[10px] text-muted-foreground">
                {formatDate(signal.timestamp)}
              </span>
            </div>

            {/* Headline */}
            <div className="flex flex-col gap-1.5">
              <span className="font-mono text-[9px] text-muted-foreground tracking-widest">HEADLINE</span>
              <h3 className="font-sans font-bold text-base leading-snug text-foreground">
                {signal.title}
              </h3>
            </div>

            {/* Executive Summary */}
            <div className="flex flex-col gap-1.5">
              <span className="font-mono text-[9px] text-muted-foreground tracking-widest">EXECUTIVE SUMMARY</span>
              <p className="font-sans text-sm text-secondary-foreground leading-relaxed">
                {signal.summary}
              </p>
            </div>

            {/* Why It Matters */}
            <div className="flex flex-col gap-1.5">
              <span className="font-mono text-[9px] text-primary tracking-widest flex items-center gap-1">
                <AlertTriangle className="w-3 h-3" /> WHY IT MATTERS
              </span>
              <p className="font-sans text-sm text-foreground leading-relaxed border-l-2 border-primary/50 pl-3 py-1 bg-primary/5 rounded-r">
                {signal.whyItMatters}
              </p>
            </div>

            {/* Key Signals */}
            <div className="flex flex-col gap-1.5">
              <span className="font-mono text-[9px] text-muted-foreground tracking-widest flex items-center gap-1">
                <Tag className="w-3 h-3" /> KEY SIGNALS
              </span>
              <div className="flex flex-wrap gap-1.5">
                {signal.tags.map((t) => (
                  <Badge key={t} variant="secondary" className="font-mono text-[9px] rounded-sm">
                    {t}
                  </Badge>
                ))}
              </div>
            </div>

            {/* Entities + Impact in columns */}
            <div className="grid grid-cols-2 gap-4">
              <div className="flex flex-col gap-1.5">
                <span className="font-mono text-[9px] text-muted-foreground tracking-widest">EXTRACTED ENTITIES</span>
                <div className="flex flex-col gap-1">
                  {signal.entities.map((e) => (
                    <span key={e} className="font-sans text-xs text-foreground flex items-center gap-1.5">
                      <span className="w-1 h-1 shrink-0 rounded-full bg-muted-foreground" />
                      {e}
                    </span>
                  ))}
                </div>
              </div>

              <div className="flex flex-col gap-1.5">
                <span className="font-mono text-[9px] text-muted-foreground tracking-widest flex items-center gap-1">
                  <ShieldAlert className="w-3 h-3" /> SYSTEM IMPACT
                </span>
                <div className="flex flex-col gap-1">
                  {signal.systemImpact.map((i) => (
                    <span key={i} className="font-sans text-xs text-secondary-foreground flex items-center gap-1.5">
                      <span className="w-1 h-1 shrink-0 rounded-full bg-primary/60" />
                      {i}
                    </span>
                  ))}
                </div>
              </div>
            </div>

            {/* Operator Takeaway */}
            <div className="flex flex-col gap-1.5 border-t border-card-border/50 pt-3">
              <span className="font-mono text-[9px] text-primary tracking-widest">OPERATOR TAKEAWAY</span>
              <p className="font-sans text-sm text-foreground leading-relaxed">
                Classification <strong>{signal.classification}</strong> at{" "}
                <strong>{signal.confidence}%</strong> confidence.{" "}
                {signal.classification === "CRITICAL"
                  ? "Immediate escalation recommended. Review system impact categories and entity list for attribution."
                  : signal.classification === "ELEVATED"
                  ? "Monitor closely. Follow up within 24 hours and cross-reference against known threat indicators."
                  : "Continue routine monitoring. Archive for pattern analysis."}
              </p>
            </div>

            {/* Footer */}
            <p className="font-mono text-[9px] text-muted-foreground/40 pt-1 border-t border-card-border/40">
              Generated by RSR Data Hub v1.0.0 · {signal.id}
            </p>
          </div>
        )}

        {/* Actions */}
        <div className="flex justify-end gap-2 pt-3 border-t border-card-border">
          <Button
            variant="outline"
            className="font-mono text-xs border-primary/40 hover:bg-primary/10"
            onClick={handleCopy}
            disabled={!signal}
            data-testid="btn-copy-brief"
          >
            <Copy className="w-3.5 h-3.5 mr-2" />
            COPY BRIEF
          </Button>
          <Button
            variant="ghost"
            className="font-mono text-xs text-muted-foreground hover:text-foreground border border-card-border"
            onClick={onClose}
            data-testid="btn-close-brief"
          >
            <X className="w-3.5 h-3.5 mr-2" />
            CLOSE
          </Button>
        </div>
      </DialogContent>
    </Dialog>
  );
}
