import {
  Dialog,
  DialogContent,
  DialogHeader,
  DialogTitle,
} from "@/components/ui/dialog";
import { Button } from "@/components/ui/button";
import { Signal } from "@/lib/mock-data";
import { Copy, X } from "lucide-react";
import { useToast } from "@/hooks/use-toast";

interface BriefModalProps {
  signal: Signal | null;
  open: boolean;
  onClose: () => void;
}

export default function BriefModal({ signal, open, onClose }: BriefModalProps) {
  const { toast } = useToast();

  if (!signal) return null;

  const formatDate = (iso: string) => {
    try {
      return new Date(iso).toUTCString();
    } catch {
      return iso;
    }
  };

  const briefText = `
INTELLIGENCE BRIEF
==================
RSR DATA HUB — SIGNAL ${signal.id}
${formatDate(signal.timestamp)}

CLASSIFICATION: ${signal.classification}
ENGINE: ${signal.engine}
CONFIDENCE: ${signal.confidence}%
SOURCE: ${signal.source}

TITLE
-----
${signal.title}

EXECUTIVE SUMMARY
-----------------
${signal.summary}

STRATEGIC SIGNIFICANCE
-----------------------
${signal.whyItMatters}

IMPACT TAGS
-----------
${signal.tags.join(" | ")}

EXTRACTED ENTITIES
------------------
${signal.entities.map((e) => `• ${e}`).join("\n")}

SYSTEM IMPACT CATEGORIES
-------------------------
${signal.systemImpact.map((i) => `• ${i}`).join("\n")}

---
Generated by RSR Data Hub v1.0.0
`.trim();

  const handleCopy = () => {
    navigator.clipboard.writeText(briefText).then(() => {
      toast({
        title: "Brief Copied",
        description: "Intelligence brief copied to clipboard",
        className: "font-mono border-primary bg-background text-foreground",
      });
    });
  };

  return (
    <Dialog open={open} onOpenChange={(v) => { if (!v) onClose(); }}>
      <DialogContent
        className="max-w-2xl bg-card border border-card-border text-foreground font-mono"
        data-testid="modal-brief"
      >
        <DialogHeader className="border-b border-card-border pb-3">
          <DialogTitle className="font-mono text-sm text-primary tracking-widest">
            INTELLIGENCE BRIEF — {signal.id}
          </DialogTitle>
        </DialogHeader>

        <div className="mt-2 flex flex-col gap-3">
          <pre
            className="text-xs text-foreground/90 leading-relaxed whitespace-pre-wrap font-mono bg-background border border-card-border rounded p-4 max-h-[60vh] overflow-y-auto"
            data-testid="text-brief-content"
          >
            {briefText}
          </pre>

          <div className="flex justify-end gap-2 pt-2">
            <Button
              variant="outline"
              className="font-mono text-xs border-primary/40 hover:bg-primary/10"
              onClick={handleCopy}
              data-testid="btn-copy-brief"
            >
              <Copy className="w-3.5 h-3.5 mr-2" />
              COPY TO CLIPBOARD
            </Button>
            <Button
              variant="ghost"
              className="font-mono text-xs text-muted-foreground hover:text-foreground border border-card-border"
              onClick={onClose}
              data-testid="btn-close-brief"
            >
              <X className="w-3.5 h-3.5 mr-2" />
              CLOSE
            </Button>
          </div>
        </div>
      </DialogContent>
    </Dialog>
  );
}
